
public class TestaFor {

	public static void main(String[] args) {
		 for( int contador = 0; contador<=10; contador++) {
			 System.out.println(contador);
		 }
	}
}
// Ao usar o "for" utilizamos o ";" dentro dos parenteses, e não no final fora deles como no "while" 
// O "For" com suas variáveis só funcionam dentro do seu escopo 
// Dentro do parenteses de "for" usamos espaço + ;  espaco e ; para atribuir TRES elementos dentro dos parenteses do "for" (ex: linha 5) 
// No primeiro espaço, geralmente o "for" contém a inicialização de quem vai ser um CONTADOR, não é regra. E ele é só executador UMA vez 
// No segundo espaço, ele deve executar todas as vezes que a função boolean para saber se ele deve entrar dentro do laço, executar a próxima iteração 
// No terceiro espaço, geralmente é ocupado por aquilo a ser executado ao fim de cada iteração

